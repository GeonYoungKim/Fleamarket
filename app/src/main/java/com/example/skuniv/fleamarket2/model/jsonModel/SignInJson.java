package com.example.skuniv.fleamarket2.model.jsonModel;

public class SignInJson {
    String id;
    String pw;

    public SignInJson(String id, String pw) {
        this.id = id;
        this.pw = pw;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getPw() {
        return pw;
    }

    public void setPw(String pw) {
        this.pw = pw;
    }
}
